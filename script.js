function pegarTexto() {
  navigator.clipboard.readText()
    .then(texto => {
      document.getElementById("texto").value = texto;
    })
    .catch(err => {
      console.log("Error al pegar:", err);
      alert("No se pudo acceder al portapapeles.");
    });
}


